#include "font.h"
#include <vector>

extern const std::vector<unsigned int> font_map;
extern const std::vector<uint8_t> font_data;
constexpr int font_data_width = 260;
constexpr int font_data_height = 70;
constexpr int font_data_grid = 10;
constexpr int font_data_chars_x = font_data_width / font_data_grid;
constexpr int font_data_chars_y = font_data_height / font_data_grid;
constexpr int font_char_width = 6;
constexpr int font_char_height = 9;

uint8_t font_data_at_xy(int x, int y) {
    return font_data[y * font_data_width + x];
}

void type(k::Machine *mach, int x, int y, std::string str, bool with_caret) {
    const auto start_x = x;
    const auto start_y = y;
    for (auto c : str) {
        type(mach, x, y, c);
        if (c == '\n') {
            y += font_char_height;
            x = start_x;
        } else {
            x += font_char_width;
            if (x >= k::screenw(mach) - font_char_width + 2) {
                y += font_char_height;
                x = start_x;
            }
        }
    }
    if (with_caret) {
        type_caret(mach, x, y);
    }
}

void type(k::Machine *mach, int x, int y, char c) {
    if (c < 32 || c > 126) {
        return;
    }
    auto map = font_map[c - 32];
    for (int y_map = 0; y_map < font_char_height - 1; ++y_map) {
        for (int x_map = 0; x_map < font_char_width - 1; ++x_map) {
            auto row = map / font_data_chars_x;
            auto column = map % font_data_chars_x;
            auto d = font_data_at_xy(column * font_data_grid + x_map,
                                     row * font_data_grid + y_map);
            if (d) {
                k::draw(mach, x + x_map, y + y_map, k::Pixel{0xFF, 0xFF, 0xFF});
            }
        }
    }
}

void type_caret(k::Machine *mach, int x, int y) {
    for (int y_map = 0; y_map < font_char_height; ++y_map) {
        for (int x_map = 0; x_map < font_char_width; ++x_map) {
            k::draw(mach, x + x_map, y + y_map, k::Pixel{0xFF, 0xFF, 0xFF});
        }
    }
}

void preview_font(k::Machine *mach, int x, int y) {
    const std::string str = " !\"#$%&'()*+,-./"
                            "0123456789:;<=>?@ABCDEFGHIJKLMNOPQRSTUVWXYZ[\\]^_`"
                            "abcdefghijklmnopqrstuvwxyz{|}~";
    type(mach, x, y, str, false);
}

